def multiply(a, b):
    """
    (int, int) -> int

    Return the multiplication of a and b

    Examples:
    >>> multiply(2, 5)
    10
    >>> multiply(-1, 3)
    -3
    >>> multiply(-3, -4)
    12
    """

    return a * b
